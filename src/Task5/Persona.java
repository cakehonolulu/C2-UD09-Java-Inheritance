package Task5;

public class Persona {

		String nombre;
		int edad;
		String sexo;
		
		public Persona() {
			this.nombre = "";
			this.edad = 0;
			this.sexo = "";
		}
		
		public Persona(String nombre, int edad, String sexo) {
			this.nombre = nombre;
			this.edad = edad;
			this.sexo = sexo;
		}
		
		
}
